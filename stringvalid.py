s = str(input())
for i in s:
    if bool(i.isalnum())==True :
        print("True")
        break
    elif i==(len(s)):
        print("False")
    else:
        print("False")
for i in s:
    if bool(i.isalpha())==True :
        print("True")
        break
    elif i==(len(s)):
        print("False")
    else:
        print("False")
for i in s:
    if bool(i.isdigit())==True :
        print("True")
        break
    elif i==(len(s)):
        print("False")
    else:
        print("False")
for i in s:
    if bool(i.islower())==True :
        print("True")
        break
    elif i==(len(s)):
        print("False")
    else:
        print("False")
for i in s:
    if bool(i.isupper())==True :
        print("True")
        break
    elif i==(len(s)):
        print("False")
    else:
        print("False")
